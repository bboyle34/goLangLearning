package main

import "fmt"

func main() {
    // strings can be added together using +
    fmt.Println("go" + "lang")

    // integers and floats
    fmt.Println("1+1 =", 1+1)
    fmt.Println("7.0/3.0 =", 7.0/3.0)

    // booleans, with boolean operators
    fmt.Println(true && false)
    fmt.Println(true || false)
    fmt.Println(!true)
}

